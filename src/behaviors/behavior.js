/******************************************************************************
 * The base class from which all behavior classes are dervied.
 *****************************************************************************/

class Behavior {

  /****************************************************************************
   * Resolves the entity's action.
   * 
   * @abstract
   ***************************************************************************/

  act() { }
  
  /****************************************************************************
   * Handles events broadcast to the behavior.
   * 
   * @abstract
   * @param {Event} event Event to handle
   ***************************************************************************/

  receive(event) { }

  /****************************************************************************
   * Handles queries made to the behavior.
   * 
   * @abstract
   * @param {Query} query Query to handle
   ***************************************************************************/

  query(query) { }

}